namespace PSFilterHostDll.Imaging
{
    internal static class FloatUtil
    {
        public static float Clamp(float value, float min, float max)
        {
            if (value < min)
            {
                return min;
            }

            if (value > max)
            {
                return max;
            }

            return value;
        }
    }
}
