using System;
using System.Runtime.InteropServices;

namespace PSFilterHostDll.Imaging
{
    [StructLayout(LayoutKind.Explicit)]
    internal struct ColorBgra8
    {
        [FieldOffset(0)]
        public byte B;

        [FieldOffset(1)]
        public byte G;

        [FieldOffset(2)]
        public byte R;

        [FieldOffset(3)]
        public byte A;

        /// <summary>
        /// Lets you change B, G, R, and A at the same time.
        /// </summary>
        [NonSerialized]
        [FieldOffset(0)]
        public uint Bgra;
    }
}
